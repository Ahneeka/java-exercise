package chapter2;

public class Calculation{
    public static void main(String[] args) {

        System.out.println("125 + 24 = " + (125 + 24));


        System.out.println("125 - 24 = " + (125 - 24));


        System.out.println("125 * 24 = " + (125 * 24));


        System.out.println("125 / 24 = " + (125 / 24));


        System.out.println("125 mod 24 = " + (125 % 24));



    }
}
