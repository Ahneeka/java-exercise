package chapter2;

public class ArithmeticsCal {


    public int squareOfNum1(int num1) {
        return num1 * num1;
    }

    public int squareOfNum2(int num2) {
        return num2 * num2;
    }

    public int sumTwoNum(int num1, int num2) {
        return num1 + num2;
    }

    public int differenceOfTwoNum(int num1, int num2) {
        return num1 - num2;
    }
}
